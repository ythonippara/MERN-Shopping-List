// ORM to interact with MongoDB database
const mongoose = require('mongoose');
// 
const Schema = mongoose.Schema;

// Create Schema
const UserSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    register_date: {
        type: Date,
        default: Date.now
    }
});

// Set up schema export
module.exports = User = mongoose.model('User', UserSchema);